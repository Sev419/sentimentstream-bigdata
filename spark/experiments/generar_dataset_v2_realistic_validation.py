def main() -> None:
    output = project_root() / "data" / "experiments" / "dataset_v2" / "labeled" / "dataset_sentimientos_v2_realistic_validation.csv"
    rows = []
    idx = 1
    for phrases, label in [(POSITIVE, "positivo"), (NEUTRAL, "neutral"), (NEGATIVE, "negativo")]:
        if len(phrases) != 100:
            raise ValueError(f"{label} tiene {len(phrases)} frases, se esperaban 100")
        for text in phrases:
            rows.append({"id": idx, "texto": text, "sentimiento": label, "fuente": "realistic_validation", "version": "v2_realistic"})
            idx += 1
    texts = [row["texto"] for row in rows]
    if len(set(texts)) != len(texts):
        raise ValueError("Duplicados exactos detectados")
    output.parent.mkdir(parents=True, exist_ok=True)
    with output.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=["id", "texto", "sentimiento", "fuente", "version"])
        writer.writeheader()
        writer.writerows(rows)
    print(f"Dataset realistic generado: {output}")
    print(f"Registros: {len(rows)}")


if __name__ == "__main__":
    main()
